# Simple Python Program

def add(x, y):
    """Adds two numbers."""
    return x + y

def main():
    """Main function to run the program."""
    x = 5  # Use fixed values for demonstration
    y = 3
    result = add(x, y)
    print(f"The result of {x} + {y} is: {result}")

if __name__ == "__main__":
    main()
